def get_url_alias(alias_key, language='zh'):
    """
    获取URL别名对应的完整URL

    Args:
        alias_key (str): 别名键
        language (str): 语言，'zh' 或 'en'

    Returns:
        str: 完整的URL，如果找不到别名则返回None
    """
    if language == 'zh':
        return ZH_URL_ALIASES.get(alias_key)
    elif language == 'en':
        return EN_URL_ALIASES.get(alias_key)
    return None


def get_all_aliases(language='zh'):
    """
    获取所有URL别名

    Args:
        language (str): 语言，'zh' 或 'en'

    Returns:
        dict: 所有别名的字典
    """
    if language == 'zh':
        return ZH_URL_ALIASES.copy()
    elif language == 'en':
        return EN_URL_ALIASES.copy()
    return {}
